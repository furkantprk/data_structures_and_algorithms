public class TableD {
        private String musteri;
        private String urun;
        private String zaman;
        private String fiyat;

        public TableD(String musteri, String urun, String zaman, String fiyat) {
            this.musteri = musteri;
            this.urun = urun;
            this.zaman = zaman;
            this.fiyat = fiyat;
        }
        
        public String getMusteri() {
            return musteri;
        }

        public String getUrun() {
            return urun;
        }

        public String getZaman() {
            return zaman;
        }

        public String getFiyat() {
            return fiyat;
        }

    

    
    }
        
    


